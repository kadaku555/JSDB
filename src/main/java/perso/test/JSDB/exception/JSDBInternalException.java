package perso.test.JSDB.exception;

public class JSDBInternalException extends JSDBException {

	public JSDBInternalException() {
		super();
	}

	public JSDBInternalException(String message) {
		super(message);
	}

	public JSDBInternalException(String message, Throwable cause) {
		super(message, cause);
	}

	public JSDBInternalException(Throwable cause) {
		super(cause);
	}
}
